let hist_array = [];

const infosc = document.querySelector("#colord");
function mudafundo() {
    //mudar cor
    let icolor = Math.floor(Math.random() * colors.length);
    document.body.style.backgroundColor = (colors[icolor]);
    document.querySelector("#colord").innerHTML = (colors[icolor] + " | N°: " + icolor)

    //infos na tela e histórico
    vzs = parseInt(vzs + 1);
    hist_array.push(colors[icolor] + " | N°: " + icolor);
    let novalinha = document.createElement('tr')
    let novainfo = document.createElement('td')
    const histab = document.querySelector('table');
    if (vzs <= 20) {
        novainfo.textContent = (hist_array[i]);
        novalinha.appendChild(novainfo)
        histab.appendChild(novalinha)
        i++
    }
    else {
        var ultimos20 = hist_array.slice(-20);
        const linhashis = document.querySelectorAll('tr');
        linhashis.forEach(tr => tr.remove())
        while (x < ultimos20.length) {
            novalinha = document.createElement('tr')
            novainfo = document.createElement('td')
            novainfo.textContent = (ultimos20[x]);
            novalinha.appendChild(novainfo)
            histab.appendChild(novalinha)
            i++
            x++
        }

    }
    x = 0;



}